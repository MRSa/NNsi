    /**
     * フォント幅を応答する
     * 
     * @param character
     * @return
     */
    public int getWidth(char character)
    {

        if (character <= 0x007f)
        {
            return (lookupWidth(character, fontWidthZone1, 6));
        }
        else if ((character >= 0x00a1)&&(character <= 0x1ff))
        {
            return (lookupWidth(character, fontWidthZone2, 12));            
        }
        else if ((character >= 0x02c6)&&(character <= 0x491))
        {
            return (lookupWidth(character, fontWidthZone3, 12));            
        }
        else if ((character >= 0x1e80)&&(character <= 0x1ef3))
        {
            return (lookupWidth(character, fontWidthZone4, 12));            
        }
        else if ((character >= 0x2010)&&(character <= 0x266b))
        {
            return (lookupWidth(character, fontWidthZone5, 12));
        }
        else if ((character >= 0x3000)&&(character <= 0x30fe))
        {
            return (lookupWidth(character, fontWidthZone6, 12));            
        }
        else if ((character >= 0xf001)&&(character <= 0xff9f))
        {
            return (lookupWidth(character, fontWidthZone7, 12));            
        }

        // テーブル内にデータが無かったとき...
        if (character < 256)
        {
            // 半角文字列...
            return (6);
        }
        return (12);        
    }
    
    /**
     * 幅の解析実施...
     * 
     * @param character
     * @param fontWidth
     * @param defaultWidth
     * @return
     */
    private int lookupWidth(char character, int[][] fontWidth, int defaultWidth)
    {
        // バイナリサーチに変更する必要あり！
        int count = 0;
        int data = fontWidth[count][0];
        while (data != 0)
        {
            if (data == character)
            {
                return (fontWidth[count][1]);
            }
            count++;
            data = fontWidth[count][0];
        }
        return (defaultWidth);
    }
